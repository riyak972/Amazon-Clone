CREATE INDEX products_search_vector_gin ON "Product" USING GIN(search_vector);
CREATE OR REPLACE FUNCTION product_search_vector_update_trigger() RETURNS trigger AS $$
BEGIN
  NEW.search_vector :=
    setweight(to_tsvector('pg_catalog.english', coalesce(NEW.title,'')), 'A') ||
    setweight(to_tsvector('pg_catalog.english', coalesce(NEW.description,'')), 'B') ||
    setweight(to_tsvector('pg_catalog.english', coalesce(NEW.brand,'')), 'C');
  RETURN NEW;
END
$$ LANGUAGE plpgsql;

CREATE TRIGGER products_search_vector_update BEFORE INSERT OR UPDATE ON "Product"
  FOR EACH ROW EXECUTE FUNCTION product_search_vector_update_trigger();
